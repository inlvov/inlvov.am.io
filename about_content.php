	HRSS is committed to upholding the highest standard in humanitarian and development space. The organization is guided by a strict code of conduct as well as other best international practices which include but not limited to: Core Humanitarian Standards (CHS) , Humanitarian Principles (Humanity, Neutrality, Impartiality and Operational independence) , Sphere Standards (Humanitarian Charter and Minimum Standards in Humanitarian response). 
