	<h4>
		<span>
		Our Core Values
	</span>
</h4>

<p>
	The core values constitute HRSS operational principles, relationship with society-the children, women, the staff, and the partners. , other NGOs and the government at local, state and GOSS. HRSS ’s attitude’ is shaped by these operational principles and incorporates the confidence to act in and on the world in a way that HRSS believes can be effective and have an impact, and an acceptance of responsibility for the social and physical conditions ‘out there’.
</p>


<p> 1.Transformation: <br>

We believe in urgent action, innovation, and the necessity of transformation – in South Sudan  and our own organization.</p>

<p> 2. Integrity: <br>
We are accountable to the people and partners we humbly serve, transparently sharing our results, stories and lessons. </p>

<p> 3. Diversity: <br>
We know that by embracing differences, actively including a variety of voices, and joining together we can solve Communities most complex problems.</p>

<p> 4. Equality: <br>

We believe in the equal value of every human being and the importance of respecting and honoring each individual; we know that change happens through people.</p>

<p> 5. Excellence: <br>

We challenge ourselves to the highest levels of learning and performance, tapping the best of human spirit to create impact</p>

<p> 6. Accountability: <br>

We hold ourselves accountable to the highest level of ethical behavior and responsibility for our actions, while maintaining integrity in our performance and advocating for same in public institutions. </p>
