<div class="th-area">
	<h4>
		<span>
		Our Approaches
	</span>
</h4>

<p>

	<ul style="list-style-type: none;">
		 



<li>1. 
    Integrated programming. We form nexus between sectors like Peace building, General Protection, WASH Shelter and NFI’s, Gender Based Violence (GBV), Food Security and Livelihood (FSL), Returnee monitoring, Refugee Settlement, Camp Coordination and Camp Management and Economic Empowerment where the outcome of humanitarian response can transition communities to resilience, build capacities and stimulate growth.</li>
<li>2. 
    Simultaneous programming. We can concentrate in one area seeking to holistically address the challenges in this area. All programs are implemented at the same time and target the same population in order to reinforce each other, leverage on resource and contribute to multiple outcomes.</li>
    <li>3. 
    Sequential programming. In disaster and natural calamity prone areas such as areas affected by floods and communal conflicts, we program in phases to ensure future risk mitigation and community preparedness such that after humanitarian response, the succeeding programs define long term solutions that minimize the disaster from recurring.</li>





	</ul>

</p>
